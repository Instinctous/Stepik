# Даны два целых числа m и n (m > n). Выведите все нечетные числа от m до n.
# (включительно) в порядке убывания.
m = int(input())
n = int(input())
for i in range(m - (m + 1) % 2, n - 1, -2):
    print(i)
# (m + 1) % 2 даёт:
# - 0, если m нечётное (тогда m - 0 = m - оставляем как есть),
# - 1, если m чётное (тогда m - 1 - предыдущее нечётное).
# range(start, stop, step) идёт от start до stop с шагом - 2, пропуская чётные.